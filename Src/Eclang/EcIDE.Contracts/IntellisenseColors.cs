namespace EcIDE.Contracts
{
    using System.Drawing;

    public class IntellisenseColors
    {
        #region Public Properties

        public static Brush CommandBlue
        {
            get
            {
                return Brushes.Blue;
            }
        }

        public static Brush AttributeGreen
        {
            get
            {
                return Brushes.DarkSeaGreen;
            }
        }

        public static Brush ClassTeal
        {
            get
            {
                return Brushes.Teal;
            }
        }

        public static Brush NumberRed
        {
            get
            {
                return Brushes.IndianRed;
            }
        }

        public static Brush CommentGreen
        {
            get
            {
                return Brushes.Green;
            }
        }

        #endregion
    }
}